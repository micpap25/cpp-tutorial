// Directives go up here
#include <iostream>

int main() {
    // std::cout lives in the iostream library
    std::cout << "Hello world!\n";
    
    // comments should be like this, above the code
    std::cout << "It is very nice to meet you!\n";
    
    // it's a lot prettier than putting it after the code
    std::cout << "Yeah!\n";

    /* it's best to comment code liberally, with a thorough
    explanation of the intent / rational of the code; don't just
    explain what the code does, explain when, why, and how */
    
    return 0;
}